INSERT INTO TB_HEROS (NAME_HERO, POWER_HERO)
VALUES  ('Flash', 'Speed'),
        ('Aquaman', 'Water'),
        ('Batman', 'Money')

--read
SELECT * FROM TB_HEROS WHERE NAME_HERO = 'Flash'

