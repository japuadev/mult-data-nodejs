CREATE TABLE IF NOT EXISTS TB_HEROS (
    ID INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY, 
    NAME_HERO TEXT NOT NULL,
    POWER_HERO TEXT NOT NULL
)

